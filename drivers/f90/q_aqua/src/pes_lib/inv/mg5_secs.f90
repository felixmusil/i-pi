SUBROUTINE mg5_secs (mxd, r, v)
integer, intent (in) :: mxd
real (kind=wp), intent (in) :: r(0:,0:)
real (kind=wp), intent (out) :: v(0:)
!-----------------------------------------------------------------------
real (kind=wp) :: pv(0:size(v)-1) !! an upper bound size
if (size(r).ne.nk*nk.or.size(v).ne.mg5_nsc(mxd)) then
 stop 'mg5_secs: bad dimensions'
endif
call mg5_isecs (mxd, r, pv)
v(0) = 1
if (3.le.mxd) then
 v(1:2) = pv(0:1)
endif
if (4.le.mxd) then
 v(3:7) = pv(2:6)
endif
if (5.le.mxd) then
 v(8:15) = pv(7:14)
endif
if (6.le.mxd) then
 v(16) = pv(0)*pv(0)
 v(17) = pv(0)*pv(1)
 v(18) = pv(1)*pv(1)
 v(19:30) = pv(15:26)
endif
if (7.le.mxd) then
 v(31) = pv(0)*pv(2)
 v(32) = pv(1)*pv(2)
 v(33) = pv(0)*pv(3)
 v(34) = pv(1)*pv(3)
 v(35) = pv(0)*pv(4)
 v(36) = pv(1)*pv(4)
 v(37) = pv(0)*pv(5)
 v(38) = pv(1)*pv(5)
 v(39) = pv(0)*pv(6)
 v(40) = pv(1)*pv(6)
 v(41:53) = pv(27:39)
endif
if (8.le.mxd) then
 v(54) = pv(3)*pv(3)
 v(55) = pv(2)*pv(4)
 v(56) = pv(3)*pv(4)
 v(57) = pv(4)*pv(4)
 v(58) = pv(2)*pv(5)
 v(59) = pv(3)*pv(5)
 v(60) = pv(4)*pv(5)
 v(61) = pv(5)*pv(5)
 v(62) = pv(2)*pv(6)
 v(63) = pv(3)*pv(6)
 v(64) = pv(4)*pv(6)
 v(65) = pv(5)*pv(6)
 v(66) = pv(6)*pv(6)
 v(67) = pv(0)*pv(7)
 v(68) = pv(1)*pv(7)
 v(69) = pv(0)*pv(8)
 v(70) = pv(1)*pv(8)
 v(71) = pv(0)*pv(9)
 v(72) = pv(1)*pv(9)
 v(73) = pv(0)*pv(10)
 v(74) = pv(1)*pv(10)
 v(75) = pv(0)*pv(11)
 v(76) = pv(1)*pv(11)
 v(77) = pv(0)*pv(12)
 v(78) = pv(1)*pv(12)
 v(79) = pv(0)*pv(13)
 v(80) = pv(1)*pv(13)
 v(81) = pv(0)*pv(14)
 v(82) = pv(1)*pv(14)
 v(83:86) = pv(40:43)
endif
if (9.le.mxd) then
 v(87) = pv(2)*pv(11)
 v(88) = pv(3)*pv(11)
 v(89) = pv(4)*pv(11)
 v(90) = pv(5)*pv(11)
 v(91) = pv(6)*pv(11)
 v(92) = pv(2)*pv(12)
 v(93) = pv(3)*pv(12)
 v(94) = pv(4)*pv(12)
 v(95) = pv(5)*pv(12)
 v(96) = pv(6)*pv(12)
 v(97) = pv(2)*pv(13)
 v(98) = pv(3)*pv(13)
 v(99) = pv(4)*pv(13)
 v(100) = pv(5)*pv(13)
 v(101) = pv(6)*pv(13)
 v(102) = pv(2)*pv(14)
 v(103) = pv(3)*pv(14)
 v(104) = pv(4)*pv(14)
 v(105) = pv(5)*pv(14)
 v(106) = pv(6)*pv(14)
 v(107) = pv(0)*pv(15)
 v(108) = pv(1)*pv(15)
 v(109) = pv(0)*pv(16)
 v(110) = pv(1)*pv(16)
 v(111) = pv(0)*pv(17)
 v(112) = pv(1)*pv(17)
 v(113) = pv(0)*pv(18)
 v(114) = pv(1)*pv(18)
 v(115) = pv(0)*pv(19)
 v(116) = pv(1)*pv(19)
 v(117) = pv(0)*pv(20)
 v(118) = pv(1)*pv(20)
 v(119) = pv(0)*pv(21)
 v(120) = pv(1)*pv(21)
 v(121) = pv(0)*pv(22)
 v(122) = pv(1)*pv(22)
 v(123) = pv(0)*pv(23)
 v(124) = pv(1)*pv(23)
 v(125) = pv(0)*pv(24)
 v(126) = pv(1)*pv(24)
 v(127) = pv(0)*pv(25)
 v(128) = pv(1)*pv(25)
 v(129) = pv(0)*pv(26)
 v(130) = pv(1)*pv(26)
 v(131:132) = pv(44:45)
endif
return
END SUBROUTINE mg5_secs
